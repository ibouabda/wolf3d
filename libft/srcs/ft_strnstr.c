#include "../incl/libft.h"

char	*ft_strnstr(const char *str, const char *to_find, size_t n)
{
	size_t	i;
	size_t	j;
	size_t	max;

	i = 0;
	j = 0;
	max = ft_strlen((char*)to_find);
	if (max <= 0)
		return ((char*)str);
	while (str[i] && i < n)
	{
		j = 0;
		while (str[i + j] == to_find[j] && i + j < n)
		{
			j++;
			if (j == max)
				return (&((char*)str)[i]);
		}
		i++;
	}
	return (0);
}
